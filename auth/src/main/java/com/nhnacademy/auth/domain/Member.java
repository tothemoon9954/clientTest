package com.nhnacademy.auth.domain;

public class Member {
    private final String id;
    private final String name;
    private final String gender;

    private final int age;

    public Member(String id, String name, String gender, int age) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.age = age;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getGender() {
        return gender;
    }

    public int getAge() {
        return age;
    }
}
